public class SwitchStatement {
    public static void main(String args[]) {

        int num = 5;

        switch (num) {
            case 1:
                System.out.println("FAN");
                break;
            case 2:
                System.out.println("TV");
                break;
            case 3:
                System.out.println("AC");
                break;
            default:
                System.out.println("all swithes are off");
        }

    }
}
