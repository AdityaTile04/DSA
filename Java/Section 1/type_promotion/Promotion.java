
public class Promotion {
    public static void main(String args[]) {
        char a = 'a';
        char b = 'b';
        System.out.println((int) a);
        System.out.println((int) b);
        System.out.println(a+b);

        byte  bt = 10;
        short sh = 30;
        char ch = 'a';
        byte ans = (byte) (bt + sh + ch);
        System.out.println(ans);

        long lg = 50;
        float fl = 30.4f;
        double db = 20.3;
        double sum = lg + fl + db;
        System.out.println(sum);
    }
}
