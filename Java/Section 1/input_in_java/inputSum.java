

import java.util.*;

public class inputSum {
    public static void main(String args[]) {
        // to calculate sum

        Scanner sc = new Scanner(System.in);

        int a = sc.nextInt();
        System.out.println("input a = " + a);

        int b = sc.nextInt();
        System.out.println("input b = " + b);

        int sum = a + b;
        System.out.println("The sum of a & b = " + sum);
    }
}
