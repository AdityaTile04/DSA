public class Assignment {
    public static void main (String args[]) {
        // Assigment operators

        int a = 10;
        a += 10; // a = a + 10
        System.out.println(a);

        int b = 20;
        b -= 10; // b = b - 10
        System.out.println(b);

        int x = 20;
        x *= 5; // x = x * 5
        System.out.println(x);

        int y = 40;
        y /= 2; // y = y / 2
        System.out.println(y);

        int A = 5;
        A %= 5; // A = A % 5
        System.out.println(A); 
    }
}