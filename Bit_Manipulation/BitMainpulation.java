public class BitMainpulation {
    public static void main(String args[]) {
        // Binary AND (&)
        System.out.println(5 & 6);

        // Binary OR (|)
        System.out.println(5 | 6);

        // Binary XOR (^)
        System.out.println(5 ^ 6);

        // Binary One's complement or NOT (~)
        System.out.println(~5);

        // Binary left shift (<<)
        int a = 6, b = 2;
        System.out.println(a << b);

        // Binary right shift (>>)
        System.out.println(a >> b);
    }
}